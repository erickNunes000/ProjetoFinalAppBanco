    private void confirmaDeposito(){
        String sql = "update conta set saldo=saldo+? where numConta=?";
        String sql2 = "insert into historico(numConta,operacao,valor) values (?,'deposito',?)";
        
        try {
          pst=conexao.prepareStatement(sql);  
          pst2 = conexao.prepareStatement(sql2);
          pst.setString(1, textoServValor.getText());//valor
          pst.setString(2, textoServNumeroConta.getText());//numConta em conta
          pst2.setString(1, textoServNumeroConta.getText());//numConta em historico
          pst2.setString(2, textoServValor.getText());//valor em historico
          int positivo = pst.executeUpdate();
          int positivo2 = pst2.executeUpdate();
          
          if(positivo>0 && positivo2>0){
              JOptionPane.showMessageDialog(null,"Operacao Deposito Realizada som Sucesso");
          }
               
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
    }
