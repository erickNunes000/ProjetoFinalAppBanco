    private void confirmaSaque(){  
        String sql = "select *from conta where numConta=?";
        //String sql2 = "insert into historico(numConta,operacao) values (?,'saque')";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, textoServNumeroConta.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                float saque = Float.parseFloat(textoServValor.getText());
                float emConta = rs.getFloat(4);
                if(emConta<saque){//se o valor do saque for maior que o valor em conta
                    JOptionPane.showMessageDialog(null,"Saldo em Conta Insuficiente");
                    textoServValor.setText(null);
                }   //ate a linha acima tudo ocorreu bem
                else{//caso contrario
                    String sql2 = "update conta set saldo=saldo-? where numConta=?";
                    String sql3 = "insert into historico(numConta,operacao,valor) values (?,'saque',?)";
                        try {  
                            pst=conexao.prepareStatement(sql2);
                            pst2 = conexao.prepareStatement(sql3);
                            pst.setString(1, textoServValor.getText());//valor
                            pst.setString(2, textoServNumeroConta.getText());//numConta em conta
                            pst2.setString(1, textoServNumeroConta.getText());//numConta em historico
                            pst2.setString(2, textoServValor.getText());
                            int yes = pst.executeUpdate();
                            int yes2 = pst2.executeUpdate();  
                            
                            if(yes>0 && yes2>0){
                                JOptionPane.showMessageDialog(null,"Operacao Saque Realizada som Sucesso");
                            }
                                  
                            
                    } catch (Exception e) {
                         JOptionPane.showMessageDialog(null, e);
                    }
                    
                }
            }
     
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
    }  
